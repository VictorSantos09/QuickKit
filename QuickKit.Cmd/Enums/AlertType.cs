namespace QuickKit.Cmd.Enums;

/// <summary>
/// Represents the type of an alert.
/// </summary>
public enum AlertType
{
    /// <summary>
    /// Represents an alert type of None.
    /// </summary>
    None,
    /// <summary>
    /// Represents a warning alert type.
    /// </summary>
    Warning,
    /// <summary>
    /// Represents an error alert type.
    /// </summary>
    Error,
    /// <summary>
    /// Represents a success alert type.
    /// </summary>
    Success
}
